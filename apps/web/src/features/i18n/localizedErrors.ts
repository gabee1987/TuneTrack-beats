const SPOTIFY_AUTH_ERROR_KEY_BY_CODE: Record<string, string> = {
  auth_denied: "error.spotify.authDenied",
  exchange_failed: "error.spotify.exchangeFailed",
  unknown: "error.spotify.unknown",
};

const PLAYLIST_IMPORT_ERROR_KEY_BY_CODE: Record<string, string> = {
  invalid_url: "error.playlist.invalidUrl",
  playlist_not_found: "error.playlist.notFound",
  playlist_private: "error.playlist.private",
  spotify_api_error: "error.playlist.spotifyApi",
  too_few_tracks: "error.playlist.tooFewTracks",
};

export function getServerErrorTranslationKey(code: string): string {
  return SERVER_ERROR_KEY_BY_CODE[code] ?? "error.server.GENERIC_ROOM_ACTION_FAILED";
}

export function localizeServerError(t: Translate, payload: ServerErrorPayload): string {
  return t(getServerErrorTranslationKey(payload.code));
}

export function localizeSpotifyAuthError(
  t: Translate,
  payload: SpotifyAuthResultPayload & { success: false },
): string {
  return t(SPOTIFY_AUTH_ERROR_KEY_BY_CODE[payload.code] ?? "error.spotify.unknown");
}

export function localizePlaylistImportError(
  t: Translate,
  payload: ImportPlaylistResultPayload & { success: false },
): string {
  return t(PLAYLIST_IMPORT_ERROR_KEY_BY_CODE[payload.code] ?? "error.playlist.spotifyApi");
}
